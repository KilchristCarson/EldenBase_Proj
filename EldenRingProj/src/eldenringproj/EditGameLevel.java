    private void glUpdateBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_glUpdateBttnActionPerformed
// Create an instance of the GameLevelHandler class
GameLevelHandler GLHand = new GameLevelHandler();

// Get values from text fields for level ID and mob count
String LevelID = txtLevelID.getText();
String MobCount = txtMobCount.getText();

try {
    // Convert level ID and mob count to integers
    int levelID = Integer.parseInt(LevelID);
    int mobCount = Integer.parseInt(MobCount);

    // Call the updateGameLevel method to update game level information
    int result = GLHand.updateGameLevel(levelID, levelID, mobCount);

    // Check if the game level was updated successfully
    if (result > 0) {
        // If successful, refresh the game level table and show a success message
        refreshGameLevelTable();
        JOptionPane.showMessageDialog(this, "Updated");
    } else {
        // If update failed, show a failure message
        JOptionPane.showMessageDialog(this, "Update Failed!");
    }
} catch (NumberFormatException e) {
    // Handle the exception for invalid integer input
    e.printStackTrace();
}


    }//GEN-LAST:event_glUpdateBttnActionPerformed

