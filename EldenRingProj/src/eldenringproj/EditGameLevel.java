    private void tblGameLevelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblGameLevelMouseClicked
        int selectedRow = tblGameLevel.getSelectedRow();
        if (selectedRow != -1) {
                    // Get values from the selected row
                    Object LevelIDValue = tblGameLevel.getValueAt(selectedRow, 0);
                    Object MobCount = tblGameLevel.getValueAt(selectedRow, 1);


                    // Set text fields with the retrieved values
                    txtLevelID.setText(String.valueOf(LevelIDValue));
                    txtMobCount.setText(String.valueOf(MobCount));

                }
    }//GEN-LAST:event_tblGameLevelMouseClicked
