    private void tblOwnedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblOwnedMouseClicked
        int selectedRow = tblOwned.getSelectedRow();
        if (selectedRow != -1) {
                    // Get values from the selected row
                    Object cIDValue = tblOwned.getValueAt(selectedRow, 0);
                    Object iIDValue = tblOwned.getValueAt(selectedRow, 1);
                    Object equippedValue = tblOwned.getValueAt(selectedRow, 2);

                    // Set text fields with the retrieved values
                    txtcID.setText(String.valueOf(cIDValue));
                    txtiID.setText(String.valueOf(iIDValue));
                    txtEquipped.setText(String.valueOf(equippedValue));
                    
                }
    }//GEN-LAST:event_tblOwnedMouseClicked
