    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// Create an instance of the OwnedHandler class
OwnedHandler OwnHand = new OwnedHandler();

// Get values from text fields for character ID, inventory ID, and equipped ID
String charID = txtcID.getText();
String invID = txtiID.getText();
String equippedID = txtEquipped.getText();

try {
    // Convert character ID and inventory ID to integers
    int intCharID = Integer.parseInt(charID);
    int intInvID = Integer.parseInt(invID);

    // Call the updateOwned method to update owned items
    int result = OwnHand.updateOwned(intCharID, intInvID, equippedID);

    // Check if the owned items were updated successfully
    if (result > 0) {
        // If successful, refresh the owned table and show a success message
        refreshOwnedTable();
        JOptionPane.showMessageDialog(this, "Updated");
    } else {
        // If update failed, show a failure message
        JOptionPane.showMessageDialog(this, "Update Failed!");
    }
} catch (NumberFormatException e) {
    // Handle the exception for invalid integer input
    e.printStackTrace();
}


    }//GEN-LAST:event_jButton1ActionPerformed

